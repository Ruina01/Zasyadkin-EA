print("Введите расстояние между рядами с дырочками:")
a = int(input())
print("Введите расстояние между дырочками в ряду:")
b = int(input())
print("Введите длину оставшегося шнурка:")
L = int(input())
print("Введите количество дырочек в ряду:")
N = int(input())
num = (2 * L + (2 * N - 1) * a + 2 * (N - 1) * b)
print("Длина шнурка:", num)
